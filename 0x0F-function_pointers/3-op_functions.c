#include "3-calc.h"
/**
*op_add - adds two numbers
*@a: first number
*@b: second number to be sum
*Return: the sum of two numbers
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
*op_sub - substract two numbers
*@a: first number
*@b: second number
*Return: the subs of two numbers
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
*op_mul - the subs of two numbers
*@a: number
*@b:number
*Return: the mult of two numbers
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
*op_div - divide two numbers
*@a:the first
*@b:the second
*Return: the result of the divided numbers
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
*op_mod - takes the module of two numbers
*@a: the first
*@b: the second
*Return: the result of the mod
*/
int op_mod(int a, int b)
{
	return (a % b);
}
